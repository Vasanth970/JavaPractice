package org.example;

public class Switch {
    public static void main(String[] args) {

        int day = 5;
        switch (day) {

            case 1:
                System.out.println("Day1");
                break;
            case 2:
                System.out.println("Day2");
                break;
            case 3:
                System.out.println("Day3");
                break;
            default:
                System.out.println("No Holiday");

        }
    }
}


