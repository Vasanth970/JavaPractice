package org.example;

public class While_Loop {
    public static void main(String[] args) {
        int countdown = 3;

        while (countdown > 0) {
            System.out.println(countdown);
            countdown--;
        }

        System.out.println("Happy New Year!!");
        /* This example counts down from 3 to 1 and then displays "Happy New Year!!" at  */
    }
}
